/**
 * Add two numbers
 * @parann a Number one
 * @paran b Number two
 * @returns Result
 */
export var add = function (a, b) { return a + b; };
/**
 * @parann a Number one
 * @paran b Number two
 * @returns Result
 */
 export var substract = function (a, b) { return a - b; };
 /**
  * Multiplies two numbers
  * @param a Number one
  * @param b Number two
  * @returns Result
  */
 export var multiply = function (a, b) { return a * b; };