/** 
*Add two numbers
* @param a Number one
* @param b Number two
* @returns Result
*/
export const add = (a: number,b: number): number => a+b;
/**
 * Substract two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
export const substract = (a: number,b: number): number => a-b;
/**
 * Multiplies two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
export const multiply = (a: number,b: number): number => a * b;